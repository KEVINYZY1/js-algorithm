function mergeSort(arr){
	var len = arr.length
	if (len==1) {
		return arr
	};
	var middle = Math.floor(len/2)
	var left= mergeSort(arr.slice(0,middle))
	var right = mergeSort(arr.slice(middle))
	return merge(left,right)

}
// 合并两个有序数组
function merge(sour,des){
	var res = [];
	while(sour.length&&des.length) {
		if (sour[0]<des[0]) {
			res.push(sour.shift())
		}else{
			res.push(des.shift())
		}
	};
	return res.concat(sour) .concat(des);
}
